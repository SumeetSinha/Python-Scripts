# /***********************************************************************************************************************
# * Sumeet Kumar Sinha [October, 2016]
# * The 0th index of ELE_TAG_DESC_ARRAY contains the information about the encoding. It basically contains the following 
# *************************************************************************************************************************
# *
# * Desc is [start_N.Nodes][end_N.Nodes][start_N.Gauss][end_N.Gauss][start_N.Element_Outputs][end_N.Element_Outputs]
# * - - - - - - - - - - - - - -- - - - - - - - -- - - - - - - - - - - - - - -- 
# *
# * [start_N.Nodes] = <num_of_digits = 1> It contains the starting digit index from which the Number of Nodes information start 
# *                                       in the Class_Desc_Array.
# *
# * [end_N.Nodes]   = <num_of_digits = 1> It contains the end digit index from which the Number of Nodes information ends in 
# *                                       the Class_Desc_Array.
# *
# * [start_N.Gauss] = <num_of_digits = 1> It contains the starting digit index from which the Number of Gauss Points information start 
# *                                       in the Class_Desc_Array.
# *
# * [end_N.Gauss]   = <num_of_digits = 1> It contains the end digit index from which the Number of Gauss Points information ends in 
# *                                       the Class_Desc_Array.
# *
# * [start_N.Ele_Outputs] = <num_of_digits = 1> It contains the starting digit index from which the Number of Element Outputs  
# *                                             information start in the Class_Desc_Array.
# *
# * [end_N.Ele_Outputs]   = <num_of_digits = 1> It contains the end digit index from which the Number of Element Outputs   
# *                                             information ends in the Class_Desc_Array.
# *
# * Default Features 
# * - - - - - -- - -
# * 1) Per gauss point there are in toltal 18 outputs of stress, plsatic strain and total strain
# * 2) No.of Outputs -> here means the extra outputs by an element except gauss points. For example:
# *					  for eight node brick there is 000 No. of Outputs. 
# ************************************************************************************************************************/


def NUMBER_OF_NODES(class_tag_desc):
	return (class_tag_desc/int(10**(9-(ELE_TAG_DESC_ENCODING/int(10.0**7))%100%10)))%(int(10**((ELE_TAG_DESC_ENCODING/int(10**7))%100%10   - (ELE_TAG_DESC_ENCODING/int(10**8))%100%10  + 1)));

def NUMBER_OF_GAUSS(class_tag_desc):
	return (class_tag_desc/int(10**(9-(ELE_TAG_DESC_ENCODING/int(10.0**5))%100%10)))%(int(10**((ELE_TAG_DESC_ENCODING/int(10**5))%100%10   - (ELE_TAG_DESC_ENCODING/int(10**6))%100%10  + 1))); 

def NUMBER_OF_ELEMENT_OUTPUTS(class_tag_desc):
	return (class_tag_desc/int(10**(9-(ELE_TAG_DESC_ENCODING/int(10.0**3))%100%10)))%(int(10**((ELE_TAG_DESC_ENCODING/int(10**3))%100%10   - (ELE_TAG_DESC_ENCODING/int(10**4))%100%10  + 1))) 


