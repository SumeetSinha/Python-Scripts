def ElementOutputInfo(class_tag):
	OutputType = None;
	OutputInfo = None;
	Status     = True;

	if(class_tag<85):
		OutputType = None;
		OutputInfo = None;	
	elif(class_tag>=85 and class_tag<=87):
		OutputType=["g_tot_t1","g_tot_t2","g_tot_n ","f_t1    ","f_t2    ","f_n     ","g_inc_t1","g_inc_t2","g_tot_n "];
		OutputInfo=["Total Slip in local x-direction","Total Slip in local y-direction","Total Penetration in contact-normal plane",\
		"Contact Force in local x-direction","Contact Force in local y-direction","Contact Force in contact-normal plane", \
		"Incremental Slip in local x-direction","Incremental Slip in local y-direction","Total Penetration in contact-normal plane"]
	elif(class_tag==ELE_TAG_Truss):
		OutputType=["del_l  ","f_axial"];
		OutputInfo=["Total Change in Length","Total Axial Force "];
	else:
		# print "Not Implemented";
		Status     = False;

	return OutputType,OutputInfo,Status;